fn horizontal_distance_to_wall(pos_x: u16, pos_y: u16, map: &Map0, angle: f32) {
    let mut constant: f32;
    // first we check the horizontal cases:
    if angle <= 180.0 {
        //ray facing up
        constant = -1.0;
    } else {
        constant = TILE_SIZE;
    }
    //buscamos la celda de la primera intersección. Osea que a la intersección la dividimos A.y/64
    let a_sub_y: u16 = ((((f32::from(pos_y) / TILE_SIZE).floor() * TILE_SIZE + constant)
        / TILE_SIZE)
        .floor()) as u16;
    let a_sub_x: u16 = pos_x + (pos_y - a_sub_y) / tan(angle);

    //teniendo la primer intersección podemos checkear la primera celda:
    let mut cell_x: f32 = a_sub_x / 64.0;
    let mut cell_y: f32 = a_sub_y / 64.0;
    //si la celda no es una pared continua el loop:
    if map[cell_x][cell_y] != 0 {
        return;
    }
    //ahora podemos ahorrarnos cálculo. sumando una constante
    //para ir saltando a las próximas celdas que nos choquemos
    if angle <= 180.0 {
        //ray facing up
        constant = -TILE_SIZE;
    } else {
        constant = TILE_SIZE;
    }
    let xa_constant = 64.0 / tan(angle);
    a_sub_x = a_sub_x + xa_constant;
    a_sub_y = a_sub_y + constant;
    //Convert this into grid coordinate by dividing each component with 64.
    cell_x = a_sub_x / 64.0; //new grid coordinate
    cell_y = a_sub_y / 64.0; // new grid coordinate
    if map[cell_x][cell_y] != 0 {
        return;
    } else {
        //repeat above
    }
    // now check for vertical collisions
    if angle <= 90.0 && angle >= 270.0 {
        //ray facing right
        constant = TILE_SIZE;
    } else {
        constant = -1.0;
    }
    let a_sub_x: u16 = ((((f32::from(pos_x) / TILE_SIZE).floor() * TILE_SIZE + constant)
        / TILE_SIZE)
        .floor()) as u16;
}


